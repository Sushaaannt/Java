package Control_Statements;

public class discount {
	 public static void main(String[] args) {
		 int num = 7000 ;
		 if (num <= 3000 && num > 2000) {
			 System.out.println("20% discount");
		 }
		 else if (num <= 5000) {
			 System.out.println("30% discount");
		 }
		 else {
			 System.out.println("50 % discount");
		 }
	}

}
